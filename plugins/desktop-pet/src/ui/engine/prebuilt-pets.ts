import type { PetBlueprint, Pixel } from './pet-blueprint'

/**
 * Hand-crafted pixel art pet blueprints at 64×64 resolution.
 * Higher resolution allows for richer detail: ears, tails, whiskers, accessories.
 */

function gridToPixels(grid: string[], paletteMap?: Record<string, number>): Pixel[] {
  const pixels: Pixel[] = []
  const map = paletteMap || {}
  for (let y = 0; y < grid.length; y++) {
    for (let x = 0; x < grid[y].length; x++) {
      const ch = grid[y][x]
      if (ch !== '.' && ch !== ' ') {
        const c = map[ch] !== undefined ? map[ch] : parseInt(ch)
        if (!isNaN(c)) pixels.push({ x, y, c })
      }
    }
  }
  return pixels
}

function partitionPixels(
  allPixels: Pixel[],
  regions: {
    headRows: [number, number]
    bodyRows: [number, number]
    legRows: [number, number]
    leftEye: { x: number; y: number; r: number }
    rightEye: { x: number; y: number; r: number }
    mouth: { x: number; y: number; r: number }
    centerX: number
  }
) {
  const head: Pixel[] = []
  const body: Pixel[] = []
  const leftLeg: Pixel[] = []
  const rightLeg: Pixel[] = []
  const leftEye: Pixel[] = []
  const rightEye: Pixel[] = []
  const mouth: Pixel[] = []

  for (const p of allPixels) {
    const dLE = Math.abs(p.x - regions.leftEye.x) + Math.abs(p.y - regions.leftEye.y)
    const dRE = Math.abs(p.x - regions.rightEye.x) + Math.abs(p.y - regions.rightEye.y)
    const dM = Math.abs(p.x - regions.mouth.x) + Math.abs(p.y - regions.mouth.y)

    if (dLE <= regions.leftEye.r && p.c === 1) { leftEye.push(p); continue }
    if (dRE <= regions.rightEye.r && p.c === 1) { rightEye.push(p); continue }
    if (dM <= regions.mouth.r && p.c === 1) { mouth.push(p); continue }

    if (p.y >= regions.legRows[0] && p.y <= regions.legRows[1]) {
      if (p.x <= regions.centerX) leftLeg.push(p)
      else rightLeg.push(p)
    } else if (p.y >= regions.headRows[0] && p.y <= regions.headRows[1]) {
      head.push(p)
    } else {
      body.push(p)
    }
  }

  return { head, body, leftLeg, rightLeg, leftEye, rightEye, mouth }
}

