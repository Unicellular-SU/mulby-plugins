// ==================== 新增：状态追踪相关类型 ====================

/**
 * 角色的持续视觉状态
 */
export interface CharacterState {
  position: string;
  pose: string;
  appearance_changes: string[];
  injuries: string[];
}

export interface CharacterStateWrapper {
  name: string;
  state: CharacterState;
}

export interface EnvironmentState {
  lighting: string;
  notable_changes: string[];
}

/**
 * 威胁实体的状态
 */
export interface ThreatState {
  /** 位置 (e.g., "in doorway", "behind protagonist") */
  position: string;
  /** 当前动作 (e.g., "reaching toward protagonist", "approaching slowly") */
  action: string;
  /** 可见程度 (e.g., "fully visible", "partially obscured", "shadow only") */
  visibility?: string;
}

/**
 * 页面的完整持续状态快照
 */
export interface PersistentStates {
  /** 各角色的当前状态 */
  characters: CharacterStateWrapper[];
  /** 环境状态 */
  environment: EnvironmentState;
  /** 威胁实体状态，key为威胁名 */
  threats?: Record<string, ThreatState>;
}

