export interface PrebuiltPetInfo {
  id: string
  name: string
  emoji: string
  palette: string[]
}

export function getPrebuiltPetList(): PrebuiltPetInfo[] {
  return PREBUILT_DEFS.map(d => ({
    id: d.id,
    name: d.name,
    emoji: d.emoji,
    palette: d.palette,
  }))
}

export function getPrebuiltBlueprint(petId: string, customPalette?: string[]): PetBlueprint | null {
  const def = PREBUILT_DEFS.find(d => d.id === petId)
  if (!def) return null

  const palette = customPalette || def.palette
  const allPixels = gridToPixels(def.grid)
  const parts = partitionPixels(allPixels, def.regions)

  return {
    id: `prebuilt_${petId}`,
    name: def.name,
    description: `预制宠物: ${def.name}`,
    palette,
    parts: {
      ...parts,
      leftArm: [],
      rightArm: [],
      tail: [],
      extras: [],
    },
    anchors: {
      eyeCenter: {
        left: { x: def.regions.leftEye.x, y: def.regions.leftEye.y },
        right: { x: def.regions.rightEye.x, y: def.regions.rightEye.y },
      },
      mouthCenter: { x: def.regions.mouth.x, y: def.regions.mouth.y },
      bodyCenter: { x: def.regions.centerX, y: Math.round((def.regions.bodyRows[0] + def.regions.bodyRows[1]) / 2) },
    },
    createdAt: Date.now(),
  }
}

export function getPrebuiltPreviewSvg(petId: string): string | null {
  const def = PREBUILT_DEFS.find(d => d.id === petId)
  if (!def) return null

  const pixels = gridToPixels(def.grid)
  const rects = pixels
    .filter(p => p.x >= 0 && p.x < 64 && p.y >= 0 && p.y < 64)
    .map(p => `<rect x="${p.x}" y="${p.y}" width="1" height="1" fill="${def.palette[p.c] || def.palette[0]}"/>`)
    .join('')

  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 64 64" shape-rendering="crispEdges">${rects}</svg>`
}
