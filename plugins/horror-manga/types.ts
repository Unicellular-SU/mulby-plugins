export interface ComicPageScript {
  page_number: number;
  layout_description: string; // Description of the grid layout
  image_prompt: string; // Prompt for the entire page image
  characters_in_scene: string[]; // List of character names present in this page
  
  // 状态追踪字段
  persistent_states?: PersistentStates;
  state_changes_this_page?: string[];
  inherited_state?: PersistentStates;
  
  /** 
   * 连续性说明（Page 2+ 必须）
   * 格式: "Continues from Page X: [what carries over]"
   */
  continuity_note?: string;
}

export interface CharacterSheetItem {
  name: string;
  description: string;
  referenceImage?: string; // Base64 image data for consistency reference
  isGenerating?: boolean; // UI tracking
}

// New Interface for the full AI response
export interface ComicResponse {
  title: string; // Comic Title
  global_art_style: string; // Just the art style (colors, lines), NO characters.
  character_sheet: CharacterSheetItem[]; // Array of character descriptions
  cover_image_prompt: string; // Specific prompt for the cover
  analysis: string;
  pages: ComicPageScript[];
}

export interface ComicMetadata {
    analysis: string;
    characterSheet: CharacterSheetItem[];
    globalArtStyle: string;
    coverPrompt: string;
    rawPages: ComicPageScript[]; 
    rawTitle: string;
}

export interface ComicPageData extends ComicPageScript {
  title?: string; // Optional title for the cover page
  imageData?: string; // Processed Base64 image (with watermark if enabled)
  rawImageData?: string; // Original Base64 image from AI (no watermark)
  isGenerating: boolean;
  error?: string;
  watermarkOverrides?: WatermarkSettings; // If present, overrides global settings for this page
}

